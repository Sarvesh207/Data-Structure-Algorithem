package com.Apnacollege;

public class CWS_15_Type_Casting {
    public static void main(String[] args) {
        //Implicit Casting
        double price = 100.00;
        double finalprice = price + 18;
        System.out.println(finalprice);

        //Explicit Casting
        int p = 100;
        int fp = p + (int )18.90;
        System.out.println(fp);

    }
}
